import Head from 'next/head';
import styles from '../styles/Home.module.css';

import { Button } from '@mui/material';
import { useEffect, useState } from 'react';
import { useSessionContext } from '@pdg/providers/SessionProvider';
import { useSupabaseContext } from '@pdg/providers/SupabaseProvider';

export default function Home() {
  const [supabase] = useSupabaseContext();
  const [session, setSession] = useSessionContext();

  useEffect(() => {
    const getSession = async () => {
      if (!supabase || !setSession) return;

      const {
        data: { session },
      } = await supabase.auth.getSession();
      setSession(session ?? undefined);
    };

    getSession();
  }, []);

  const signInWithOAuth = async () => {
    await supabase?.auth.signInWithOAuth({
      provider: 'google',
    });
  };

  const signOut = async () => {
    if (!setSession) return;

    await supabase?.auth.signOut();
    setSession(undefined);
  };

  return (
    <>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <main className={styles.main}>
        {session ? (
          <Button onClick={signOut}>Logout</Button>
        ) : (
          <Button onClick={signInWithOAuth}>Sign in with Google</Button>
        )}
      </main>
    </>
  );
}
